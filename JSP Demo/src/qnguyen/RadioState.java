package qnguyen;

public class RadioState {
    String yesCheck;
    String noCheck;

    public RadioState() {
        this.yesCheck = "";
        this.noCheck = "";
    }

    public String getYesCheck() {
        return yesCheck;
    }

    public void setYesCheck(String yesCheck) {
        this.yesCheck = yesCheck;
    }

    public String getNoCheck() {
        return noCheck;
    }

    public void setNoCheck(String noCheck) {
        this.noCheck = noCheck;
    }
}
